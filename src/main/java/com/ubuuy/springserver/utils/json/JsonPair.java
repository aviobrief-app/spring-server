package com.ubuuy.springserver.utils.json;

public class JsonPair {

    private String key;
    private String value;

    public JsonPair() {
    }

    String getKey() {
        return key;
    }

    void setKey(String key) {
        this.key = key;
    }

    String getValue() {
        return value;
    }

    void setValue(String value) {
        this.value = value;
    }

}
